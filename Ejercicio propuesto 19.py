lado_triangulo_equilatero = float(input('Ingrese el valor del lado del triángulo equilátero'))

print('Perímetro del triángulo equilatero: ', 3 * lado_triangulo_equilatero)
print('Altura del triángulo equilatero: ', lado_triangulo_equilatero * (3 ** (1/2)) / 2)
print('Área del triángulo equilatero: ', (lado_triangulo_equilatero ** 2) * (3 ** (1/2)) / 4)